function tabSwitch(new_tab, new_content) {
     
    document.getElementById('chart-1').style.display = 'none';
    document.getElementById('chart-2').style.display = 'none';
    document.getElementById('chart-3').style.display = 'none';        
    document.getElementById(new_content).style.display = 'block';   
     
 
    document.getElementById('tab-1').className = '';
    document.getElementById('tab-2').className = '';
    document.getElementById('tab-3').className = '';        
    document.getElementById(new_tab).className = 'active-tab';      
 
}